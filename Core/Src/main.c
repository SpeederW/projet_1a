// Interrupt handler
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	switch(GPIO_Pin) {
		case BUTTON_Push_Pin:
			HAL_GPIO_TogglePin(GPIOB, LED_Pin);
			LCD_Update_Required = 1;
			break;
		case BUTTON_Selector_Incr_Pin:
			HAL_GPIO_TogglePin(GPIOB, LED_Pin);
			counter++;
			LCD_Update_Required = 1;
			break;
		case BUTTON_Selector_Decr_Pin:
			HAL_GPIO_TogglePin(GPIOB, LED_Pin);
			counter--;
			LCD_Update_Required = 1;
			break;
		default:
			LCD_Update_Required = 0;
			break;
	}
}

